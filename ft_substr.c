#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	i;
	char	*sub;
	size_t	j;

	i = 0;
	j = 0;
	sub = (char *)malloc(sizeof(*s) * (len + 1));
	while (sub == NULL)
	{
		return (NULL);
	}
	while (s[i] != '\0')
	{
		if (i >= start && j < len)
		{
			sub[j] = s[i];
			j++;
		}
		i++;
	}
	sub[j] = '\0';
	return (sub);
}
